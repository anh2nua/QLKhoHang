    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        if (tblNhacc.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Trong bảng chưa có dữ liệu");
            return;
        } else {
            index = tblNhacc.getRowCount();
            for (int i = 0; i < index; i++) {
                if (tblNhacc.getValueAt(i, 0).toString().equalsIgnoreCase(txtTimKiem.getText())) {
                    showDetail(i);
                    JOptionPane.showMessageDialog(this, "Bạn đã tìm được Nhà cung cấp : " + tblNhacc.getValueAt(i, 1).toString());
                    break;
                }
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

