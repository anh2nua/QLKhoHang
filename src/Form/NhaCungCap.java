    private boolean checkNull() {
        if (txtMaNhaCC.getText().isEmpty()) {
            txtMaNhaCC.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Mã nhà cung cấp trống");
            return false;
        } else if (txtTenNhaCC.getText().isEmpty()) {
            txtTenNhaCC.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Tên nhà cung cấp trống");
            return false;
        } else if (txtDiaChi.getText().isEmpty()) {
            txtDiaChi.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Địa chỉ trống");
            return false;
        } else if (txtEmaiil.getText().isEmpty()) {
            txtEmaiil.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Email trống");
            return false;
        } else if (txtSDT.getText().isEmpty()) {
            txtSDT.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Số điện thoại trống");
            return false;
        } else if ((txtEmaiil.getText().endsWith(".com") || txtEmaiil.getText().endsWith(".COM") || txtEmaiil.getText().endsWith(".Com") || txtEmaiil.getText().endsWith(".COm") || txtEmaiil.getText().endsWith(".cOM") || txtEmaiil.getText().endsWith(".coM")) == false) {
            txtEmaiil.requestFocus();
            JOptionPane.showMessageDialog(this, "Email phải kết thúc bằng đuôi \".com\"");
            return false;
        } else {
            return true;
        }
    }

